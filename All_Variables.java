package JavaTutorial;

public class All_Variables {

	public static void main(String[] args) {
		
		/*
		 * Byte
		 * Min -128
		 * Max -127
		 * Default - 0
		 * */
		byte bytevariables=100;
		System.out.println("Print the Byte value :" +bytevariables);
		
		/*
		 * Short
		 * Min : -32768
		 * Max : 32767
		 * Default : 0
		 */
		Short Shortvariable=1000;
		System.out.println("Print the Short value: " +Shortvariable);
		
		/*
		 * int
		 * Min : -2147483648
		 * Max : 2147483647
		 * Default :
		 */
		int intvariable=10000;
		System.out.println("Print the integer value : " +intvariable);
		
		/*
		 *long
		 * Min : -2^63
		 * Max : 2^63
		 * Default : 0
		 */
		long longvariable=1000000L;
		System.out.println("Print the long variable value : " +longvariable);
		
		/*
		 * float
		 * Default : 0.0f 
		 */
		
		float floatvariable=20.0f;
		System.out.println("Print the float variable value :" +floatvariable);
		
		/*
		 * double
		 * default =0.0d
		 */
		double doublevariable=20.20;
		System.out.println("Print the double variable value :" +doublevariable);
		
		/*
		 * boolean
		 * false and true
		 * default : false
		 */
		boolean booleanvariable=true;
		System.out.println("print the boolean variable value : " +booleanvariable);
		
		/*
		 * char
		 * Min : 0
		 * Max : 65,535
		 * default :
		 */
				
		char charvariable = 'R';
		System.out.println("Print the char variable value : " +charvariable);
	}

}
